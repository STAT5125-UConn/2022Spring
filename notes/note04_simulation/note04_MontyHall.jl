results

# ‚ïî‚ïê‚ï° 3a47e437-a8e3-4af6-9a91-a9ecb19e3d45
begin
	p_not = mean([any(results[i] .== "üö™üëàüöó") for i in 1:n])
md"""
If yo don't switch, the proportion of finding the car is: $(p_not)
"""
end

# ‚ïî‚ïê‚ï° 22b1cc5d-5be9-4868-ba80-0a57da66e733

