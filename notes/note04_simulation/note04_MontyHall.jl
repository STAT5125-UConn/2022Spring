# ‚ïî‚ïê‚ï° 4fe838bd-93b6-41a6-8e6f-560c8f326a42
begin
    using Random, Statistics 
    using PlutoUI, Images, StatsBase # , Distributions
    Random.seed!(1)
    
    function whichDoor(choice; nds=3)
        prize = rand(1:nds)
        if prize == choice
            host = rand(setdiff(1:nds, choice))
        else
            host = rand(setdiff(1:nds, [choice, prize]))
        end
        switch = rand(setdiff(1:nds, [choice, host]))
        return (host=host, prize=prize, switch=switch)
    end

    door = load("images/Wooden_Door_PNG_Clip_Art-2401.png")
    goat = load("images/Goat_PNG_Clipart.png")
    goat = imresize(goat, round.(Int64, 200 .* size(goat) ./ size(goat, 2)))
    car = load("images/Red_Cool_Ferrari_Dino_Car_PNG_Clipart-123R.png")
    car = imresize(car, round.(Int64, 200 .* size(car) ./ size(car, 2)))
    hand = load("images/Tuching_Finger_Hand_PNG_Clip_Art-1619.png")
    (nc, pc), (ng, pg) = size(car), size(goat)
    (nd, p),  (nh, ph) = size(door), size(hand)
    loc_car = (nd-nc)√∑2 .+ (1:nc)
    loc_goat = (nd-ng)√∑2 .+ (1:ng)
    loc_hand = (nd-nh)√∑2 .+ (1:nh)
    doors = [door door door]
	  mh, mc, mg = (p .- (ph, pc, pg)) .√∑ 2
	  doors
end

# ‚ïî‚ïê‚ï° d7073324-9bbe-11eb-3acc-17af3ed6547f
md"""# Monty Hall problem

Suppose you're on a game show, and you're given the choice of three doors:
Behind one door is a car; behind the others, goats. You pick a door, say No. 1,
and the host, who knows what's behind the doors, opens another door, say No. 3,
which has a goat. He then says to you, "Do you want to pick door No. 2?" Is it
to your advantage to switch your choice?
"""

# ‚ïî‚ïê‚ï° 597d02a5-218f-40d5-9c3b-2f5def20351d
md"""## Simulation

We use a simulation to find the answer.
"""

# ‚ïî‚ïê‚ï° f892fe78-f6a6-42db-84c5-0b65a17b9d9a
md"""
Your choice = $(@bind choice NumberField(1:1:3))
"""

# ‚ïî‚ïê‚ï° fef08663-3232-4906-b587-35db52582996
md"""
Now the host open a door with goat. 
$(@bind showhost Select(["hide", "show"])) 
"""

# ‚ïî‚ïê‚ï° c9a7af1b-51c1-4f32-a597-9aa76dab7559
begin
	onegame = whichDoor(choice)
	doors_open1 = repeat(door, outer=(1,3))
	doors_open1[loc_hand,(choice-1)*p.+(1+mh:p-mh)] = hand
	doors_open1[loc_goat,(onegame.host-1)*p.+(1+mg:p-mg)] = goat
	showhost == "show" ? doors_open1 : nothing
end

# ‚ïî‚ïê‚ï° b109f6d9-b3f2-42ef-a3f9-e6c0a81bdb40
md"""
Do you want to switch to door number *$(setdiff(1:3, [choice, onegame.host]))*?

Now let's see where the car is. $(@bind showcar Select(["hide", "show"])) 
"""

# ‚ïî‚ïê‚ï° 1df9e00c-2495-4a78-9a82-9b4a18e1ef42
begin
	doors_open2 = copy(doors_open1)
	doors_open2[loc_goat,(onegame.host-1)*p.+(1+mg:p-mg)] = goat
	doors_open2[loc_car,(onegame.prize-1)*p.+(1+mc:p-mc)] = car
	showcar == "show" ? doors_open2 : nothing
end

# ‚ïî‚ïê‚ï° 67562e44-2f38-48c5-b5c2-de351e4334c8
md"""
Let's run the game many times to approximate the probabilities.
"""

# ‚ïî‚ïê‚ï° 6675eb47-35f5-4f0e-82cd-895f7db6e38e
md"""
number of doors = $(@bind nds NumberField(3:5)); 
number of games = $(@bind n NumberField(10:1000))
"""

# ‚ïî‚ïê‚ï° 13165617-6a15-492c-8603-ba1307409d20
begin
	# nds = 3
	# n = 100
	results = Vector{Any}(undef, n)
	for i in eachindex(results)
	    doors = fill("üö™", nds)
	    choice = rand(1:nds)#1
	    doors[choice] *= "üëà"
	    onegame = whichDoor(choice, nds=nds)
	    doors[onegame.host] *= "üêê"
	    doors[onegame.switch] *= "üëå"
	    doors[onegame.prize] *= "üöó"
	    results[i] = doors
	end
end

